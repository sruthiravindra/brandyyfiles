using System;
using System.Collections.Generic;
using System.Data;
using System.Data.SqlClient;
using System.Linq;
using System.Web;
using System.Web.UI;
using System.Web.UI.WebControls;

public partial class brands_Default4 : System.Web.UI.Page
{
    public static ConnectionClass ConnObj = null;
    public int Cnt;

    #region First and last calling
    ProjectInitUnloadCalling _ProjectInitUnloadCalling = new ProjectInitUnloadCalling();
    protected void Page_Init(Object sender, EventArgs e)
    {
        _ProjectInitUnloadCalling.Page_Init();
    }
    protected void Page_Unload(object sender, EventArgs e)
    {
        _ProjectInitUnloadCalling.Page_Unload();
        ReleaseInstance();
    }
    private void ReleaseInstance()
    {
        if (ConnObj != null)
        {
            ConnObj.ReleaseConnection();
        }

    }
    private void CreateInstance()
    {
        if (ConnObj == null)
        {
            ConnObj = new ConnectionClass();
        }
    }
    protected void Page_Load(object sender, EventArgs e)
    {
        CreateInstance();

        if ((!Page.IsPostBack) && (SessionState._BrandAdmin != null))
        {
        }
        else if (SessionState._BrandAdmin != null)
        {
            ;
        }
        else
        {
            Response.Redirect(SessionState.WebsiteURLBrand);
        }

    }
    #endregion
    protected void chkSubs_CheckedChanged(object sender, EventArgs e)
    {
        SqlCommand cmd = new SqlCommand("sp_Get_Brands_HidePopupSteps");
        cmd.Parameters.AddWithValue("@user_id", SessionState._BrandAdmin.user_id);
        ConnObj.ExecuteNonQuery(cmd);
        //Page.ClientScript.RegisterClientScriptBlock(this.GetType(), "CloseWindowScript", "window.close();", true);
    }
}